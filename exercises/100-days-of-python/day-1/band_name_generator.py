# 1. Create a greeting for your program.

print(
    "Hi, this is a band name generator. Provide your childhood city and first",
    "pet's name for your new band name.",
)

# 2. Ask the user for the city that they grew up in.

city = input("What city did you grow up in?\n")

# 3. Ask the user for the name of a pet.

pet_name = input("And what was the name of your first pet?\n")

# 4. Combine the name of their city and pet and show them their band name.

print("Great, your band name is: " + city + " " + pet_name + ". Ta-da!")

